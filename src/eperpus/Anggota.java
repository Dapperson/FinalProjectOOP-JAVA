    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        if((txtId.getText().equals(""))||(txtNama.getText().equals(""))||(txtAlamat.getText().equals(""))||(txtTtl.getText().equals("")))
            {
                JOptionPane.showMessageDialog(btnSimpan, "Data Ada Yang Kosong", "INFORMASI", WIDTH, null);
            }
            else 
            {
                Connection c=buka_koneksi(); //panggil function koneksi
         String sqlkode="Insert into anggota(id,nama,alamat,ttl,jkl) values ('"+this.txtId.getText()+"',"
                     + "'"+this.txtNama.getText()+"',"+ "'"+this.txtAlamat.getText()+"',"+"'"+this.txtTtl.getText()+"',"
                 +"'"+this.cmbJenis.getSelectedItem()+"')";
            try { //jalankan query tersebut
               PreparedStatement p2=(PreparedStatement) c.prepareStatement(sqlkode);
               p2.executeUpdate();
               p2.close();
               
               //tampilkan data
                tampilkandata();
                JOptionPane.showMessageDialog(this, "Simpan Berhasil","Informasi",1);
                cmbJenis.setSelectedItem("Laki-Laki");
                txtId.setText("");
                txtNama.setText("");
                txtAlamat.setText("");
                txtTtl.setText("");
                
                btnHapus.setEnabled(true);
                btnEdit.setEnabled(true);
                
                txtId.requestFocus();
           
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Terjadi kesalahan "+ex.getMessage());
            }
            }
    }//GEN-LAST:event_btnSimpanActionPerformed

