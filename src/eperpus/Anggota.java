    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        if(txtId.getText().equals("")){
              JOptionPane.showMessageDialog(this, "Text ID Kosong","Informasi",1);
              txtId.requestFocus();
          }
        else{
        DefaultTableModel tabelanggota = new DefaultTableModel();
        tabelanggota.addColumn("Id");
        tabelanggota.addColumn("Nama");
        tabelanggota.addColumn("Alamat");
        tabelanggota.addColumn("Tanggal Lahir");
        tabelanggota.addColumn("Jenis Kelamin");
        
        try {
            buka_koneksi();
            String sql = "Select * from anggota where id like '%" +txtId.getText()+
                    "%'" +"or nama like '%" + txtId.getText() + "%'"; 
            Statement stat = koneksi.createStatement();
            ResultSet res=stat.executeQuery(sql);
            
            cmbJenis.setSelectedItem("Laki-Laki");
            txtId.setText("");
            txtNama.setText("");
            txtAlamat.setText("");
            txtTtl.setText("");
            btnHapus.setEnabled(true);
            btnEdit.setEnabled(true);
            txtId.requestFocus();
            while (res.next()) {
                tabelanggota.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5)});
            }
           tblAnggota.setModel(tabelanggota);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Data Tidak Ditemukan ");
        }
        }
    }//GEN-LAST:event_btnCariActionPerformed

